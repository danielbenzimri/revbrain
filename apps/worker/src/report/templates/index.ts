/**
 * Report Template — assembles all sections into a complete HTML document.
 *
 * Each section is a pure function: ReportData → HTML string.
 * CSS is inlined for PDF rendering (no external stylesheets).
 */

import type { ReportData } from '../assembler.ts';
import { reportStyles } from './styles.ts';
import { escapeHtml, badge, scoreBar, severity, table } from './partials/helpers.ts';

export function renderReport(data: ReportData): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <style>${reportStyles}</style>
</head>
<body>
  ${renderCover(data)}
  ${renderScope(data)}
  ${renderExecutiveSummary(data)}
  ${renderGlance(data)}
  ${renderSettings(data)}
  ${renderLifecycle(data)}
  ${renderConfigDomain(data)}
  ${renderUsage(data)}
  ${renderDataQuality(data)}
  ${renderCustomCode(data)}
  ${renderHotspots(data)}
  ${renderAppendixA(data)}
  ${renderAppendixB(data)}
  ${renderAppendixCD(data)}
</body>
</html>`;
}

// ============================================================================
// Section 1: Cover Page
// ============================================================================

function renderCover(data: ReportData): string {
  return `
  <div class="cover">
    <h1>SALESFORCE CPQ<br/>ENVIRONMENT ASSESSMENT REPORT</h1>
    <h2>Current State Configuration & Usage Analysis<br/>90-Day Activity Window</h2>
    ${table(
      ['Field', 'Value'],
      [
        ['Client Name', escapeHtml(data.metadata.clientName)],
        ['Salesforce Org ID', escapeHtml(data.metadata.orgId)],
        ['Environment', escapeHtml(data.metadata.environment)],
        ['Assessment Date', escapeHtml(data.metadata.assessmentDate)],
        ['Assessment Period', escapeHtml(data.metadata.assessmentPeriod)],
        ['CPQ Package Version', escapeHtml(data.metadata.cpqVersion)],
        ['Report Generated By', escapeHtml(data.metadata.generatedBy)],
        ['Classification', 'Confidential'],
      ]
    )}
    <div class="section-footer">Generated by RevBrain CPQ Assessment Tool</div>
  </div>`;
}

// ============================================================================
// Section 2: Scope & Methodology
// ============================================================================

function renderScope(_data: ReportData): string {
  return `
  <div class="page-break">
    <h2>1. Scope & Methodology</h2>
    <h3>1.1 Assessment Scope</h3>
    <p>This report provides a current-state assessment of the Salesforce CPQ environment. It inventories all configuration objects, analyzes transactional usage and user behavior over the preceding 90 days, evaluates data quality and technical debt, and identifies areas of concentrated complexity.</p>

    <h3>1.2 Methodology</h3>
    <ol>
      <li><strong>Metadata Extraction:</strong> CPQ configuration metadata extracted via Salesforce APIs.</li>
      <li><strong>Usage & Behavior Analysis:</strong> SOQL queries extract 90-day transactional data.</li>
      <li><strong>Data Quality Assessment:</strong> Automated analysis of field completeness and orphaned records.</li>
      <li><strong>Custom Code Scan:</strong> Identification of Apex classes, triggers, and flows referencing SBQQ objects.</li>
      <li><strong>Complexity Scoring:</strong> Fixed weighted model normalized against benchmark ranges.</li>
    </ol>

    <h3>1.3 Data Confidence Levels</h3>
    ${table(
      ['Level', 'Label', 'Definition'],
      [
        ['★★★', badge('Confirmed'), 'Directly extracted from Salesforce metadata or SOQL query.'],
        ['★★', badge('Estimated'), 'Derived from available metadata with reasonable inference.'],
        ['★', badge('Partial'), 'Based on incomplete extraction or sampling.'],
      ]
    )}
  </div>`;
}

// ============================================================================
// Section 3: Executive Summary
// ============================================================================

function renderExecutiveSummary(data: ReportData): string {
  const findingsRows = data.executiveSummary.keyFindings.map((f, i) => [
    String(i + 1),
    `<strong>${escapeHtml(f.title)}</strong>`,
    escapeHtml(f.detail),
    badge(f.confidence),
  ]);

  const scores = data.executiveSummary.complexityScores;

  return `
  <div class="page-break">
    <h2>2. Executive Summary</h2>
    <h3>2.1 Key Findings</h3>
    ${table(['#', 'Finding', 'Detail', 'Confidence'], findingsRows)}

    <h3>2.2 Complexity Scores</h3>
    ${scoreBar('Overall Complexity', scores.overall)}
    ${scoreBar('Configuration Depth', scores.configurationDepth)}
    ${scoreBar('Pricing Logic', scores.pricingLogic)}
    ${scoreBar('Customization Level', scores.customizationLevel)}
    ${scoreBar('Data Volume & Usage', scores.dataVolumeUsage)}
    ${scoreBar('Technical Debt', scores.technicalDebt)}
  </div>`;
}

// ============================================================================
// Section 4: CPQ at a Glance
// ============================================================================

function renderGlance(data: ReportData): string {
  const sections = Object.entries(data.cpqAtAGlance);

  return `
  <div class="page-break">
    <h2>3. CPQ at a Glance</h2>
    <div class="glance-grid">
      ${sections
        .map(
          ([title, metrics]) => `
        <div class="glance-section">
          <h4>${escapeHtml(title)}</h4>
          ${table(
            ['Metric', 'Value', 'Conf.'],
            metrics.map((m) => [
              escapeHtml(m.label),
              `<strong>${escapeHtml(m.value)}</strong>`,
              badge(m.confidence),
            ])
          )}
        </div>
      `
        )
        .join('')}
    </div>
  </div>`;
}

