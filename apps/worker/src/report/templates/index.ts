function renderCover(data: ReportData): string {
  const rows: string[][] = [
    ['Client Name', escapeHtml(data.metadata.clientName)],
    ['Salesforce Org ID', escapeHtml(data.metadata.orgId)],
    ['Environment', escapeHtml(data.metadata.environment)],
    ['Assessment Date', escapeHtml(data.metadata.assessmentDate)],
    ['Assessment Period', escapeHtml(data.metadata.assessmentPeriod)],
    ['CPQ Package Version', escapeHtml(data.metadata.cpqVersion)],
    [
      'Adv. Approvals Version',
      data.metadata.sbaaVersion ? escapeHtml(data.metadata.sbaaVersion) : 'Not installed',
    ],
    ['Document Version', escapeHtml(data.metadata.documentVersion)],
    ['Report Generated By', escapeHtml(data.metadata.generatedBy)],
    ['Classification', 'Confidential'],
  ];

  return `
  <div class="cover">
    <h1>SALESFORCE CPQ<br/>ENVIRONMENT ASSESSMENT REPORT</h1>
    <h2>Current State Configuration & Usage Analysis</h2>
    ${table(['Field', 'Value'], rows)}
    <div class="section-footer">Generated by RevBrain CPQ Assessment Tool</div>
  </div>`;
}

