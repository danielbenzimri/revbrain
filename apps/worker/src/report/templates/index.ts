// Section 4: CPQ at a Glance
// ============================================================================

function renderGlance(data: ReportData): string {
  const sections = Object.entries(data.cpqAtAGlance);
  const confSummary = data.dataConfidenceSummary;

  return `
  <div class="page-break">
    <h2>3. CPQ at a Glance</h2>
    ${confSummary ? `<p><em>Data confidence summary: approximately ${confSummary.confirmed}% Confirmed, ${confSummary.estimated}% Estimated, ${confSummary.partial}% Partial.</em></p>` : ''}
    <div class="glance-grid">
      ${sections
        .map(
          ([title, metrics]) => `
        <div class="glance-section">
          <h4>${escapeHtml(title)}</h4>
          ${table(
            ['Metric', 'Value', 'Conf.'],
            metrics.map((m) => [
              escapeHtml(m.label),
              `<strong>${escapeHtml(m.value)}</strong>`,
              badge(m.confidence),
            ])
          )}
          ${
            title === 'Product Catalog' &&
            metrics.some((m) => m.label === 'Bundle-capable Products' && m.value !== '0')
              ? `<p style="font-size:0.85em;color:#666;margin-top:4px;"><em>Bundle-capable = products with SBQQ__ConfigurationType__c set. This is a metadata capability flag, not a count of fully configured bundles with nested options.</em></p>`
              : ''
          }
        </div>
      `
        )
        .join('')}
    </div>
  </div>`;
}

