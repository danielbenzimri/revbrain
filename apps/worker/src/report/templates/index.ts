function renderExecutiveSummary(data: ReportData): string {
  const findingsRows = data.executiveSummary.keyFindings.map((f, i) => [
    String(i + 1),
    `<strong>${escapeHtml(f.title)}</strong>`,
    escapeHtml(f.detail),
    badge(f.confidence),
  ]);

  const scores = data.executiveSummary.complexityScores;

  return `
  <div class="page-break">
    <h2>2. Executive Summary</h2>
    <h3>2.1 Key Findings</h3>
    ${table(['#', 'Finding', 'Detail', 'Confidence'], findingsRows)}

    <h3>2.2 Complexity Scores</h3>
    ${scoreBar('Overall Complexity', scores.overall)}
    ${scoreBar('Configuration Depth', scores.configurationDepth)}
    ${scoreBar('Pricing Logic', scores.pricingLogic)}
    ${scoreBar('Customization Level', scores.customizationLevel)}
    ${scoreBar('Data Volume & Usage', scores.dataVolumeUsage)}
    ${scoreBar('Technical Debt', scores.technicalDebt)}
    <p><em>Scores are directional indicators of relative complexity, not absolute measures.</em></p>

    <h3>2.3 Scoring Methodology</h3>
    ${table(
      ['Dimension', 'Weight', 'Score', 'Drivers'],
      data.executiveSummary.scoringMethodology.map((m) => [
        escapeHtml(m.dimension),
        `${m.weight}%`,
        `${m.score}/100`,
        `${escapeHtml(m.drivers)}<br/><em style="font-size:0.9em;color:#555;">${escapeHtml(m.rationale)}</em>`,
      ])
    )}
  </div>`;
}

