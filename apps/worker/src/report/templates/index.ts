/**
 * Report Template — assembles all sections into a complete HTML document.
 *
 * Each section is a pure function: ReportData → HTML string.
 * CSS is inlined for PDF rendering (no external stylesheets).
 *
 * Updated: Redline mitigation R0–R2 (scoring methodology, tech debt,
 *          feature utilization, expanded scope, active filtering, coverage)
 */

import type { ReportData } from '../assembler.ts';
import { isSectionEnabled } from '../assembler.ts';
import { reportStyles } from './styles.ts';
import { escapeHtml, badge, scoreBar, severity, table } from './partials/helpers.ts';
import {
  renderCheckboxTable,
  renderBinaryCheckboxTable,
  renderDenominatorFootnote,
} from './partials/checkbox.ts';
import { renderSummaryBoxPair } from './partials/summary-box.ts';

export function renderReport(data: ReportData): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <style>${reportStyles}</style>
</head>
<body>
  ${renderCover(data)}
  ${renderBanners(data)}
  ${renderScope(data)}
  ${renderExecutiveSummary(data)}
  ${renderGlance(data)}
  ${renderSettings(data)}
  ${renderLifecycle(data)}
  ${renderSection6Header(data)}
  ${renderProductCatalog(data)}
  ${isSectionEnabled('6.2', data) ? renderProductDeepDive(data) : ''}
  ${renderPriceRules(data)}
  ${renderProductRules(data)}
  ${renderDiscountSchedules(data)}
  ${isSectionEnabled('6.6', data) ? renderBundlesDeepDive(data) : ''}
  ${renderApprovalsAndDocs(data)}
  ${renderUsage(data)}
  ${renderDataQuality(data)}
  ${renderCustomCode(data)}
  ${isSectionEnabled('10', data) ? renderRelatedFunctionality(data) : ''}
  ${renderHotspots(data)}
  ${renderAppendixA(data)}
  ${renderAppendixB(data)}
  ${renderAppendixCD(data)}
  ${isSectionEnabled('appendixE', data) ? renderAppendixE(data) : ''}
</body>
</html>`;
}

