// Section 10: Custom Code & Automation
// ============================================================================

function renderCustomCode(data: ReportData): string {
  return `
  <div class="page-break">
    <h2>9. Custom Code & Automation Inventory</h2>

    <h3>9.1 Apex Classes Referencing SBQQ</h3>
    ${table(
      ['Class Name', 'Lines', 'Purpose', 'Origin'],
      data.customCode.apexClasses.map((a) => [
        escapeHtml(a.name),
        String(a.lines),
        escapeHtml(a.purpose),
        escapeHtml(a.origin),
      ])
    )}

    ${(() => {
      // §9.1a — Third-party (managed package) Apex summary.
      // Default view is one row per namespace (vendor). Per-class
      // detail is suppressed because managed-package Apex migrates
      // via a vendor plan, not via Apex rewrite. Internal diagnostic
      // runs may set `showThirdPartyApexDetail: true` which also
      // populates `thirdPartyApexDetail` below.
      if (data.customCode.thirdPartyApex.length === 0) return '';
      const total = data.customCode.thirdPartyApex.reduce((sum, ns) => sum + ns.classCount, 0);
      const summary = `
    <h3>9.1a Third-Party (Managed Package) Apex Classes</h3>
    <p><em>${total} managed-package Apex classes across ${data.customCode.thirdPartyApex.length} installed vendor package(s). These do not migrate via Apex rewrite — each vendor requires a separate migration plan.</em></p>
    ${table(
      ['Managed Package Namespace', 'Class Count', 'Total Lines'],
      data.customCode.thirdPartyApex.map((ns) => [
        escapeHtml(ns.namespace),
        String(ns.classCount),
        String(ns.totalLines),
      ])
    )}`;
      if (data.customCode.thirdPartyApexDetail.length === 0) return summary;
      return `${summary}
    <h4>9.1a.1 Per-Class Detail (Internal Diagnostic Mode)</h4>
    ${table(
      ['Class Name', 'Namespace', 'Lines'],
      data.customCode.thirdPartyApexDetail.map((c) => [
        escapeHtml(c.name),
        escapeHtml(c.namespace),
        String(c.lines),
      ])
    )}`;
    })()}

    <h3>9.2 Triggers & Flows</h3>
    ${
      data.counts.flowCountActive > 0
        ? `<p><em>${data.counts.flowCountActive} active flows (${data.counts.flowCountCpqRelated} CPQ-related${data.counts.flowCountActive > data.counts.flowCountCpqRelated ? `, ${data.counts.flowCountActive - data.counts.flowCountCpqRelated} additional active flows in org` : ''}). ${data.counts.flowCountActive} active flows shown. Total flows in org (including inactive and managed package flows) may be higher.</em></p>`
        : ''
    }
    ${
      data.customCode.triggersFlows.length > 0
        ? table(
            ['Component', 'Type', 'Object', 'Status'],
            data.customCode.triggersFlows.map((t) => [
              escapeHtml(t.name),
              escapeHtml(t.type),
              escapeHtml(t.object),
              escapeHtml(t.status),
            ])
          )
        : '<p><em>No triggers or flows detected.</em></p>'
    }

    <h3>9.3 Validation Rules</h3>
    ${
      data.customCode.validationRules.length > 0
        ? (() => {
            const objects = [
              ...new Set(data.customCode.validationRules.map((v) => v.object)),
            ].filter((o) => o && o !== 'CPQ Object');
            return `<p><em>${data.customCode.validationRules.length} validation rules across ${objects.length > 0 ? `${objects.length} CPQ-related objects (${objects.join(', ')})` : 'CPQ-related objects'}.</em></p>`;
          })()
        : ''
    }
    ${
      data.customCode.validationRules.length > 0
        ? table(
            ['Object', 'Rule', 'Status', 'Complexity'],
            data.customCode.validationRules.map((v) => [
              escapeHtml(v.object),
              escapeHtml(v.rule),
              escapeHtml(v.status),
              severity(v.complexity),
            ])
          )
        : '<p><em>No validation rules detected.</em></p>'
    }

    ${
      data.customCode.permissionSets.length > 0
        ? `
    <h3>9.4 CPQ Permission Sets</h3>
    ${table(
      ['Permission Set', 'Type', 'Namespace'],
      data.customCode.permissionSets.map((ps) => [
        escapeHtml(ps.name),
        escapeHtml(ps.type),
        escapeHtml(ps.namespace),
      ])
    )}`
        : ''
    }
  </div>`;
}

