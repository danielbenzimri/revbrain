// Section 6.2: Product Deep Dive [T2 — conditional] (R-04)
// ============================================================================

function renderProductDeepDive(data: ReportData): string {
  const dd = data.productDeepDive;
  if (!dd) return '';

  const summaryBoxes = renderSummaryBoxPair(
    {
      title: 'Product Catalog',
      items: [
        { label: 'Active Products', value: String(dd.summary.activeProducts) },
        { label: 'Inactive Products', value: String(dd.summary.inactiveProducts) },
        { label: 'Bundle-capable Products', value: String(dd.summary.bundleCapableProducts) },
        { label: 'Price Books', value: String(dd.summary.priceBooks) },
      ],
    },
    {
      title: 'Technical Debt Indicators',
      items: [{ label: 'Dormant Products', value: dd.summary.dormantPercent }],
    }
  );

  const pricingTable = table(
    ['Method', 'Count', '% of Active', 'Complexity'],
    dd.pricingMethodDistribution.map((p) => [
      escapeHtml(p.method),
      String(p.count),
      escapeHtml(p.percentOfActive),
      escapeHtml(p.complexity),
    ])
  );

  const subTable = table(
    ['Type', 'Count', '% of Active', 'Notes'],
    dd.subscriptionProfile.map((s) => [
      escapeHtml(s.type),
      String(s.count),
      escapeHtml(s.percentOfActive),
      escapeHtml(s.notes),
    ])
  );

  return `
  <div class="page-break">
    <h2>6.2 Product Deep Dive</h2>
    <p><em>Product Summary (Active Only) — ${dd.summary.activeProducts} Products</em></p>
    ${summaryBoxes}

    ${renderCheckboxTable(
      dd.fieldUtilization,
      '6.2.1 Product Field Utilization',
      `% = products with field populated / ${dd.denominatorLabel}`
    )}

    <h4>6.2.2 Pricing Method Distribution</h4>
    ${pricingTable}
    ${renderDenominatorFootnote('products with pricing method', 'active products', dd.summary.activeProducts)}

    <h4>6.2.3 Subscription Profile</h4>
    ${subTable}
    ${renderDenominatorFootnote('products with subscription type', 'active products', dd.summary.activeProducts)}
  </div>`;
}

