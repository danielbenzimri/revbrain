function renderUsage(data: ReportData): string {
  return `
  <div class="page-break">
    <h2>7. Usage & Adoption Analysis</h2>

    <h3>7.1 90-Day Quoting Activity</h3>
    ${table(
      ['Metric', 'Value', 'Trend', 'Conf.'],
      data.usageAdoption.quotingActivity.map((m) => [
        escapeHtml(m.metric),
        `<strong>${escapeHtml(m.value)}</strong>`,
        escapeHtml(m.trend),
        badge(m.confidence),
      ])
    )}

    <h3>7.2 Conversion by Quote Size</h3>
    ${
      data.usageAdoption.conversionBySize.length > 0
        ? table(
            ['Size', '% Quotes', '% Revenue', 'Conversion', 'Conf.'],
            data.usageAdoption.conversionBySize.map((s) => [
              escapeHtml(s.segment),
              `${s.percentQuotes}%`,
              `${s.percentRevenue}%`,
              `${s.conversionRate}%`,
              badge(s.confidence),
            ])
          )
        : '<p><em>No conversion data available.</em></p>'
    }

    <h3>7.3 Discount Distribution</h3>
    ${
      data.usageAdoption.discountDistribution.length > 0
        ? table(
            ['Range', 'Count', '% Discounted'],
            data.usageAdoption.discountDistribution.map((d) => [
              escapeHtml(d.range),
              String(d.count),
              `${d.percent}%`,
            ])
          )
        : '<p><em>No discount data available.</em></p>'
    }

    ${
      data.usageAdoption.overrideAnalysis
        ? `
    <h3>7.4 Manual Price Overrides</h3>
    <p>${data.usageAdoption.overrideAnalysis.count} lines (${data.usageAdoption.overrideAnalysis.rate}%) with manual overrides. Revenue impact: $${data.usageAdoption.overrideAnalysis.revenueImpact.toLocaleString()}.</p>
    `
        : ''
    }

    <h3>7.5 User Behavior by Role</h3>
    ${
      data.usageAdoption.userBehavior.length > 0
        ? table(
            ['Role', 'Users', '% Quotes', 'Conv %'],
            data.usageAdoption.userBehavior.map((u) => [
              escapeHtml(u.profile),
              String(u.users),
              `${u.percentQuotes}%`,
              `${u.conversionRate}%`,
            ])
          )
        : '<p><em>No user behavior data available.</em></p>'
    }

    <h3>7.6 Top Quoted Products</h3>
    ${
      data.usageAdoption.topProducts.length > 0
        ? table(
            ['#', 'Product', 'Category', 'Quoted', '% Quotes'],
            data.usageAdoption.topProducts.map((p, i) => [
              String(i + 1),
              escapeHtml(p.name),
              escapeHtml(p.category),
              String(p.quotedCount),
              `${p.percentQuotes}%`,
            ])
          )
        : '<p><em>No product data available.</em></p>'
    }
  </div>`;
}

