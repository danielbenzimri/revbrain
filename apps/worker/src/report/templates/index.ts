function renderProductCatalog(data: ReportData): string {
  const dormant = data.configurationDomain.dormantFamilies;
  return `
  <div>
    <h3>6.1 Product Catalog</h3>
    ${
      data.configurationDomain.productCatalog.length > 0
        ? table(
            ['Category', 'Active', 'Inactive', 'Quoted 90d', '% Quoted', 'Conf.'],
            data.configurationDomain.productCatalog.map((c) => [
              escapeHtml(c.category),
              String(c.active),
              String(c.inactive),
              String(c.quoted90d),
              escapeHtml(c.percentQuoted),
              badge(c.confidence),
            ])
          ) +
          `<p style="font-size:0.85em;color:#666;margin-top:4px;"><em>% Quoted = products quoted in 90-day window &divide; active products in category.</em></p>`
        : '<p><em>Product catalog breakdown available from Catalog collector.</em></p>'
    }
    ${
      dormant.length > 0
        ? `<p><em>${dormant.length} of ${data.configurationDomain.productCatalog.length} product families show zero quoting activity in the 90-day window — including ${dormant
            .slice(0, 3)
            .map((d) => d.name)
            .join(
              ', '
            )}${dormant.length > 3 ? ', and others' : ''}. This may indicate dormant catalog segments, seasonal patterns, or a narrow active use-case.</em></p>`
        : ''
    }
  </div>`;
}

