function renderBundlesDeepDive(data: ReportData): string {
  const dd = data.bundlesDeepDive;
  if (!dd) return '';

  const summaryBoxes = renderSummaryBoxPair(
    {
      title: 'Bundle Configuration',
      items: [
        { label: 'Bundle-capable', value: String(dd.summary.bundleCapable) },
        { label: 'Configured Bundles', value: String(dd.summary.configuredBundles) },
        { label: 'Nested Bundles', value: String(dd.summary.nestedBundles) },
        { label: 'Avg Options/Bundle', value: dd.summary.avgOptionsPerBundle },
      ],
    },
    {
      title: 'Option Complexity',
      items: [
        { label: 'Total Options', value: String(dd.summary.totalOptions) },
        { label: 'Options w/Constraints', value: dd.summary.optionsWithConstraintsPercent },
        { label: 'Config Attributes', value: dd.summary.configAttributesPercent },
        { label: 'Config Rules', value: dd.summary.configRulesPercent },
      ],
    }
  );

  const bundleFootnote =
    dd.summary.bundleCapable !== dd.summary.configuredBundles
      ? `<p style="font-size:11px;color:#718096;margin-top:4px;"><em>Bundle-capable = products with SBQQ__ConfigurationType__c set (Required or Allowed). Configured Bundles = bundle-capable products that have at least one active SBQQ__ProductOption__c child record. The difference (${dd.summary.bundleCapable - dd.summary.configuredBundles}) reflects bundle-capable products with no options currently attached.</em></p>`
      : '';

  return `
  <div class="page-break">
    <h2>6.6 Bundles &amp; Options Deep Dive</h2>
    ${summaryBoxes}
    ${bundleFootnote}

    ${renderCheckboxTable(
      dd.relatedObjectUtilization,
      '6.6.1 Related Object Utilization',
      `% = count / ${dd.denominatorLabel}`
    )}
  </div>`;
}

